using System;

//Float or Double?

class FloatOrDouble
{
    static void Main()
    {
        double firstVal = 34.567839023;
        Console.WriteLine("The first value is, double: {0}", firstVal);
        float secondVal = 12.345F;
        Console.WriteLine("The second value is, float: {0}", secondVal);
        double thirdVal = 8923.1234857;
        Console.WriteLine("The third value is, double: {0}", thirdVal);
        float fourthVal = 3456.091F;
        Console.WriteLine("The fourth value is, float: {0}", fourthVal);
    }
}
